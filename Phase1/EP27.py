#range start i at 0 // *** stop at len - 1

for i in range(3) : #(start = 0 , stop = 3 , i++)
    print("i = ", i)

print("\n")

for i in range(1,3) :  #(start = 1 , stop = 3 , i++)
    print("i = " , i)

print("\n")

for i in range(1,6,2) : #(start = 1 , stop = 6 , i+= 2)
    print("i = " , i)